namespace Age.Vulkan.Types;

/// <summary>
/// Structure specifying a three-dimensional extent.
/// </summary>
public struct VkExtent3D
{
    /// <summary>
    /// The width of the extent.
    /// </summary>
    public uint width;

    /// <summary>
    /// The height of the extent.
    /// </summary>
    public uint height;

    /// <summary>
    /// The depth of the extent.
    /// </summary>
    public uint depth;
}
